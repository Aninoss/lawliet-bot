package events.sync.apidata.v1;

public class FisheryGearLevels {

    private int fishingRod;
    private int fishingRobot;
    private int fishingNet;
    private int metalDetector;
    private int role;
    private int profitFromSurveys;
    private int work;

    public int getFishingRod() {
        return fishingRod;
    }

    public void setFishingRod(int fishingRod) {
        this.fishingRod = fishingRod;
    }

    public int getFishingRobot() {
        return fishingRobot;
    }

    public void setFishingRobot(int fishingRobot) {
        this.fishingRobot = fishingRobot;
    }

    public int getFishingNet() {
        return fishingNet;
    }

    public void setFishingNet(int fishingNet) {
        this.fishingNet = fishingNet;
    }

    public int getMetalDetector() {
        return metalDetector;
    }

    public void setMetalDetector(int metalDetector) {
        this.metalDetector = metalDetector;
    }

    public int getRole() {
        return role;
    }

    public void setRole(int role) {
        this.role = role;
    }

    public int getProfitFromSurveys() {
        return profitFromSurveys;
    }

    public void setProfitFromSurveys(int profitFromSurveys) {
        this.profitFromSurveys = profitFromSurveys;
    }

    public int getWork() {
        return work;
    }

    public void setWork(int work) {
        this.work = work;
    }

}
